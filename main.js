// Numbers
const num1 = 10;
const num2 = 20;
let value;

//  Операторы   + - * / %
value = num1 + num2;
value = value + 100;
value += 100;     //   Использовать эту запись
// %
value = 5 % 2;    // остаток от деления ( 1 )
// Инкремент / декремент увел/уменьш переменную на 1
value++;   // изменения произойдут на следующей строке 
value--;
++value;   // изменения произойдут на этой строке
--value;

console.log(value);